package ExceptionHandling;

public class ReadMeFirst {
	
	/******
	 
	Exception handling is a mechanism to handle runtime errors such as
	ClassNotFoundException, IOException, SQLException, RemoteEception so that normal flow 
	of the application can be maintained
	
	We basically use try and catch block in java exception handling
	The try block has code that is expected to run as normally
	catch block holds code that is run after the program catches an exception
	
	Then there is a finally block that runs regardless of what
	
	****Hierarchy of java exceptions********
	
	Throwable->Eception-> (IOException, SQLException, ClassNotFoundException and RuntimeException)
	From RuntimeException->(ArithmeticException, NullPointerException, NumberFormatException and 
	IndexOutOfBoundsException->(ArrayIndexOutOfBoundsException and StringIndexOutOfBoundsException))
	
	*Error->(StackOverflowError, VirtualMachineError, OutOfMemoryError)
	 
	 *****/

}
