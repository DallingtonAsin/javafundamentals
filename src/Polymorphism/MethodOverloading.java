package Polymorphism;

/*  Method Overloading is one way of implementing polymorphism(compile-time polymorphism) */
public class MethodOverloading {
	
	// Key thing here is that method signatures are different
	
	public int add(int x, int y) {
		return x+y;
	}
	
	public double add(double x, double y) {
		return x+y;
	}

	

}
